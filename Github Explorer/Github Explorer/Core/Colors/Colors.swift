import UIKit

extension UIColor {
    
    // MARK: - Colors
    
    /// Green Primary
    /// #68C151
    static var greenPrimary: UIColor {
        return UIColor(hexString: "#68C151")
    }
    
    /// Ligh Gray
    /// #EBEDEF
    static var lightGray: UIColor {
        return  UIColor(hexString: "#EBEDEF")
    }
}
